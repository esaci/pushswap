#include "libft.h"

int	ft_isalnum(int c)
{
	if (c >= 'a' && c <= 'z')
		return (1);
	if (c <= 'Z' && c >= 'A')
		return (1);
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
